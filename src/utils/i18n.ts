let currentLang: LangCode = 'en';

export function initI18n() {
    const saved = loadGM('xflow_language', '') as string;
    if (saved && TRANSLATIONS[saved as LangCode]) {
        currentLang = saved as LangCode;
        return;
    }

    const navLangs = navigator.languages || [navigator.language];
    for (const lang of navLangs) {
        const clean = lang.toLowerCase();
        if (clean.startsWith('zh-cn') || clean === 'zh' || clean.startsWith('zh-sg')) {
            currentLang = 'zh-CN';
            return;
        }
        if (clean.startsWith('zh-tw') || clean.startsWith('zh-hk') || clean.startsWith('zh-mo')) {
            currentLang = 'zh-TW';
            return;
        }
        if (clean.startsWith('ja')) {
            currentLang = 'ja';
            return;
        }
        if (clean.startsWith('ko')) {
            currentLang = 'ko';
            return;
        }
        if (clean.startsWith('vi')) {
            currentLang = 'vi';
            return;
        }
    }
    
    currentLang = 'en';
}

export function getLang(): LangCode {
    return currentLang;
}

export function setLang(lang: LangCode) {
    if (TRANSLATIONS[lang]) {
        currentLang = lang;
        saveGM('xflow_language', lang);
    }
}

export function t(key: string): string {
    return TRANSLATIONS[currentLang][key] ?? TRANSLATIONS['en'][key] ?? key;
}

export function tLabel(label: string): string {
    const clean = label.trim();
    const key = LABEL_KEY_MAP[clean];
    if (key) {
        return t(key);
    }
    return clean;
}
